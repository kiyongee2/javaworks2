package reference;

public class Subject {
	String subjectName;
	int scorePoint;
	
	public void setSubjectName(String name) {
		subjectName = name;
	}
	
	public String getSubjectName() {
		return subjectName;
	}
	
	public void setScorePoint(int score) {
		scorePoint = score;
	}
	
	public int getScorePoint() {
		return scorePoint;
	}
}
