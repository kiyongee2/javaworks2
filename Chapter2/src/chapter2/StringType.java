package chapter2;

public class StringType {

	public static void main(String[] args) {
		//문자열 자료형
		String s = "k";  //문자열일 경우 "" 붙임.
		
		String name = "Ja" + "va";
		String str = name + 8.0;
		
		System.out.println(s);
		System.out.println(name);
		System.out.println(str);
		System.out.println(3 + "4");
	}
}
